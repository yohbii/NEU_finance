-- 基金公司表：记录基金公司基本信息
CREATE TABLE company
(
    companyId       INT PRIMARY KEY AUTO_INCREMENT, -- 公司ID，主键，自增
    companyName     VARCHAR(100) NOT NULL,          -- 公司名称
    licenseNo       VARCHAR(50),                    -- 公司牌照编号
    establishedDate DATE                            -- 成立时间
);

-- 基金经理表：记录基金经理的基本信息
CREATE TABLE manager
(
    managerId       INT PRIMARY KEY AUTO_INCREMENT, -- 基金经理ID，主键，自增
    managerName     VARCHAR(100) NOT NULL,          -- 基金经理姓名
    birthYear       INT,                            -- 出生年份
    experienceYears INT,                            -- 从业年限
    totalAum        DECIMAL(20, 2)                  -- 管理总资产（单位：亿元）
);

-- 基金表：记录每一只基金的基本信息
CREATE TABLE fund
(
    fundCode      VARCHAR(20) PRIMARY KEY, -- 基金代码，主键
    fundName      VARCHAR(100),            -- 基金名称
    companyId     INT,                     -- 所属基金公司ID
    managerId     INT,                     -- 基金经理ID
    fundType      VARCHAR(50),             -- 基金类型（如：混合型、债券型）
    inceptionDate DATE,                    -- 基金成立日期
    latestNav     DECIMAL(10, 4),          -- 最新单位净值
    navDate       DATE,                    -- 净值对应日期
    riskLevel     VARCHAR(20),             -- 风险等级（如：高、中、低）

    -- 外键：关联公司表
    FOREIGN KEY (companyId) REFERENCES company (companyId),
    -- 外键：关联经理表
    FOREIGN KEY (managerId) REFERENCES manager (managerId)
);

-- 基金标签表：记录标签名称（如：高波动、明星基金等）
CREATE TABLE fundTag
(
    tagId   INT PRIMARY KEY AUTO_INCREMENT, -- 标签ID，主键
    tagName VARCHAR(100) NOT NULL UNIQUE    -- 标签名称，唯一
);

-- 基金与标签的多对多关系表
CREATE TABLE fundTagRelation
(
    fundCode VARCHAR(20),          -- 基金代码
    tagId    INT,                  -- 标签ID

    PRIMARY KEY (fundCode, tagId), -- 联合主键

    -- 外键：基金代码
    FOREIGN KEY (fundCode) REFERENCES fund (fundCode),
    -- 外键：标签ID
    FOREIGN KEY (tagId) REFERENCES fundTag (tagId)
);

-- 基金组合表：用户保存的基金组合
CREATE TABLE portfolio
(
    portfolioId   INT PRIMARY KEY AUTO_INCREMENT,      -- 组合ID，主键，自增
    portfolioName VARCHAR(100) NOT NULL,               -- 组合名称
    createdBy     VARCHAR(100),                        -- 创建人
    createdAt     TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- 创建时间
    note          TEXT                                 -- 备注信息（可选）
);

-- 组合与基金之间的多对多关系表
CREATE TABLE portfolioFund
(
    portfolioId INT,                     -- 基金组合ID
    fundCode    VARCHAR(20),             -- 基金代码
    weight      DECIMAL(5, 2),           -- 权重（百分比）

    PRIMARY KEY (portfolioId, fundCode), -- 联合主键

    -- 外键：组合ID
    FOREIGN KEY (portfolioId) REFERENCES portfolio (portfolioId),
    -- 外键：基金代码
    FOREIGN KEY (fundCode) REFERENCES fund (fundCode)
);

-- 基金净值历史表：记录每日单位净值，用于回测和收益分析
CREATE TABLE fundNav
(
    fundCode VARCHAR(20),                             -- 基金代码
    navDate  DATE,                                    -- 净值日期
    navValue DECIMAL(10, 4),                          -- 单位净值

    PRIMARY KEY (fundCode, navDate),                  -- 联合主键

    FOREIGN KEY (fundCode) REFERENCES fund (fundCode) -- 外键：基金代码

);


